-- ============================================================
-- 把 N 卡的 "新人 X 號" 名字改成像球員的假外文中譯名
-- 維持跟 R/SR/SSR 命名風格一致
-- ============================================================

-- 假名池（80 個、跟 R/SR 卡的命名風格一致：日韓 / 拉丁 / 斯拉夫 / 北歐 假音譯）
WITH names AS (
  SELECT row_number() OVER () - 1 AS idx, name FROM (VALUES
    ('卡爾佩'),('莫里斯'),('德加可'),('帕奇科'),('沃爾沙'),('賓尼塔'),('科爾佐'),('馬丁霍'),
    ('福格曼'),('巴爾達'),('瓦爾尼茲'),('葛拉佐'),('阿圖羅'),('費迪南'),('米提奇'),('柯瓦塞夫'),
    ('扎伊塞'),('卡馬爾'),('德雷柯'),('佩雷拉'),('科斯丁'),('奧爾梅斯'),('比西尼'),('庫提斯'),
    ('波卡爾'),('多納佐'),('迪馬可'),('哈隆斯'),('帕茲尼'),('賽爾沃'),('特沃林'),('烏奇耶'),
    ('范布林'),('溫切斯'),('葉爾科'),('扎卡諾'),('奧索里'),('莫卡諾'),('科爾努'),('佩什科'),
    ('卡德林'),('蘇巴沙'),('費連戈'),('巴塞托'),('恰倫斯'),('迪比沃'),('赫南德'),('馬西拉'),
    ('波丹塔'),('科拉茲'),('赫多倫'),('馬諾韋'),('奇爾頓'),('多瑞茲'),('帕茲奇'),('沃倫德'),
    ('賽巴可'),('佐裡奇'),('柯魯尼'),('卡馬廷'),('烏茲克'),('莫斯卡'),('德拉斯'),('鄭俊翰'),
    ('金敏宇'),('朴賢哲'),('林志雄'),('黃宇翔'),('陳承翰'),('吳孝振'),('李正宇'),('蘇義文'),
    ('松井遼'),('佐野祐'),('高田航'),('安藤涉'),('村上凌'),('森下勇'),('福井輝'),('長谷敦')
  ) AS t(name)
)
UPDATE player_card_pool pc
SET name = nm.name
FROM (
  SELECT card_id, ROW_NUMBER() OVER (ORDER BY card_id) - 1 AS r
  FROM player_card_pool
  WHERE rarity = 'N' AND name LIKE '新人%'
) seq
JOIN names nm ON nm.idx = seq.r % 80
WHERE pc.card_id = seq.card_id;
