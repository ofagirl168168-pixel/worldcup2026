#!/usr/bin/env node
/**
 * 把 js/data-articles.js、js/epl-data-articles.js、js/ucl-data-articles.js 裡的
 * 文章 metadata 抽出來 → data/articles-index.mjs
 *
 * 產出供 functions/article/[id].js、functions/sitemap.xml.js 使用。
 * shareable = 日期 >= 今天 - 7 天（或未來日期）。
 *
 * 使用方式：
 *   node scripts/build-articles-index.js
 */

const fs = require('fs');
const path = require('path');
const vm = require('vm');

function extract(file) {
  const code = fs.readFileSync(file, 'utf8');
  // 用 IIFE 包起來，const 在函式 scope 可以被 return 出來
  const wrapped = `
    (function(){
      const window = {};
      ${code}
      return {
        wc:  (typeof ARTICLES !== 'undefined')     ? ARTICLES     : null,
        epl: (typeof EPL_ARTICLES !== 'undefined') ? EPL_ARTICLES : null,
        ucl: (typeof UCL_ARTICLES !== 'undefined') ? UCL_ARTICLES : null
      };
    })();
  `;
  const ctx = { console };
  vm.createContext(ctx);
  return vm.runInContext(wrapped, ctx);
}

function stripHtml(s) {
  return String(s || '').replace(/<[^>]+>/g, ' ').replace(/\s+/g, ' ').trim();
}

const files = [
  'js/data-articles.js',
  'js/epl-data-articles.js',
  'js/ucl-data-articles.js',
];

const all = [];

for (const f of files) {
  const full = path.resolve(f);
  if (!fs.existsSync(full)) { console.warn(`skip missing: ${f}`); continue; }
  const r = extract(full);

  if (r.wc) {
    for (const a of r.wc) {
      all.push({
        id: String(a.id),
        tournament: 'wc',
        title: a.title,
        summary: stripHtml(a.desc).slice(0, 220),
        date: a.date,
        category: a.cat || '',
        readTime: a.readTime || null,
      });
    }
  }
  if (r.epl) {
    for (const a of r.epl) {
      all.push({
        id: a.id,
        tournament: 'epl',
        title: a.title,
        summary: stripHtml(a.summary).slice(0, 220),
        date: a.date,
        category: a.category || '',
        readTime: null,
      });
    }
  }
  if (r.ucl) {
    for (const a of r.ucl) {
      all.push({
        id: a.id,
        tournament: 'ucl',
        title: a.title,
        summary: stripHtml(a.summary).slice(0, 220),
        date: a.date,
        category: a.category || '',
        readTime: null,
      });
    }
  }
}

// 標記 shareable（近 7 天 + 未來）
const today = new Date();
today.setHours(0, 0, 0, 0);
const sevenAgo = new Date(today);
sevenAgo.setDate(sevenAgo.getDate() - 7);

for (const a of all) {
  const d = new Date(a.date + 'T00:00:00');
  a.shareable = isFinite(d) && d >= sevenAgo;
}

// 依日期排序（新到舊）
all.sort((a, b) => b.date.localeCompare(a.date));

const outDir = path.resolve('data');
if (!fs.existsSync(outDir)) fs.mkdirSync(outDir, { recursive: true });

const out = `// AUTO-GENERATED by scripts/build-articles-index.js — do not edit by hand
// Regenerate with: node scripts/build-articles-index.js
export default ${JSON.stringify(all, null, 2)};
`;
fs.writeFileSync(path.join(outDir, 'articles-index.mjs'), out);

const shareable = all.filter(a => a.shareable).length;
console.log(`✅ Indexed ${all.length} articles (${shareable} shareable within 7-day window)`);
console.log(`   Wrote data/articles-index.mjs`);
